function word(str) {
	if( str=='zero'){return 0}
	if( str=='one'){return 1}
	if( str=='two'){return 2}
	if( str=='three'){return 3}
	if( str=='four'){return 4}
	if( str=='five'){return 5}
	if( str=='six'){return 6}
	if( str=='seven'){return 7}
	if( str=='eight'){return 8}
	if( str=='nine'){return 9}
}



